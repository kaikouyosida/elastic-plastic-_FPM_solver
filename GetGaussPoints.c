#include <stdio.h>
#include <stdlib.h>
#include "GetGaussPoints.h"


// 積分点数N_quのときのガウス点と重み係数を準備 //
void Gauss_points_and_weighting_factors(int N_qu, double* X, double* w){
	if(N_qu == 1){
		w[0] = 2.0; X[0] = 0.;
	}
	else if(N_qu == 2){
		w[0] = 1.0; X[0] = 0.5773502691896258;
		w[1] = 1.0; X[1] = -0.5773502691896258;
	}
	else if(N_qu == 3){
		w[0] = 5.0/9.0; X[0] = 0.7745966692414834;
		w[1] = 8.0/9.0; X[1] = 0.;
		w[2] = 5.0/9.0; X[2] = -0.7745966692414834;
	}
	else if(N_qu == 4){
		w[0] = 0.3478548451374539; X[0] = 0.8611363115940526;
		w[1] = 0.6521451548625461; X[1] = 0.3399810435848563;
		w[2] = 0.6521451548625461; X[2] = -0.3399810435848563;
		w[3] = 0.3478548451374539; X[3] = -0.8611363115940526;
	}
	else if(N_qu == 5){
		w[0] = 0.2369268850561891; X[0] = 0.906179845938664;
		w[1] = 0.4786286704993665; X[1] = 0.5384693101056831;
		w[2] = 128.0/225.0;        X[2] = 0.;
		w[3] = 0.4786286704993665; X[3] = -0.5384693101056831;
		w[4] = 0.2369268850561891; X[4] = -0.906179845938664;
	}
	else{
		printf("Required number of gauss node is too many\n");
		exit(-1);
	}
}